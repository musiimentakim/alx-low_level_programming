#include "main.h"
/**
 * print_square - Function
 * @size: Size of the shape
 * Description: ' function that prints a square'
 * Return: None
 */

void print_square(int size)
{
	int i, j;

	if (size > 0)
	{
		for (i = 0; i < size; i++)
		{
			for (j = 0; j < size; j++)
			{
				_putchar('#');
			}
			_putchar('\n');
		}
	}
	else
	{
		_putchar('\n');
	}
}
